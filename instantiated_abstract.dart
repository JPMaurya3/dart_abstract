//Can an abstract class be instantiated in Dart? Why or why not?
// no abstract class not instatinted , only we can implement using subclass and override the abstract method

/*No, an abstract class cannot be instantiated in Dart. 
This is because an abstract class is meant to serve as a 
blueprint for other classes rather than 
a fully functional object. It often contains abstract methods 
(methods without an implementation) that must be implemented by 
any subclass that extends the abstract class. To use the functionality
 of an abstract class, you must create a subclass that provides concrete 
 implementations of the abstract methods. */